// Transfer Fee를 랜덤으로 설정하는 함수
export const getRandomTransferFee = (overallAbility: number): number => {
    const range = transferFeeRanges[overallAbility] || [0, 0]; // 범위가 없을 경우 0으로 설정
    const [min, max] = range;

    // 범위 내에서 랜덤 값을 구하고 10000으로 나눈 후 다시 곱해 만단위 이하를 버림
    const randomValue = Math.floor((Math.random() * (max - min + 1) + min) / 10000) * 10000;

    return randomValue; // 만단위까지만 남긴 값을 반환
};
